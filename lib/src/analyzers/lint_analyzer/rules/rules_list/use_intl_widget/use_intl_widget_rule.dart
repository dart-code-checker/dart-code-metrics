class UseIntlWidget extends FlutterRule {
  static const String ruleId = 'use-intl-widget';

  UseIntlWidget([Map<String, Object> config = const {}])
      : super(
          id: ruleId,
          severity: readSeverity(config, Severity.error),
          excludes: readExcludes(config),
          includes: readIncludes(config),
        );

  @override
  Iterable<Issue> check(InternalResolvedUnitResult source) {
    final _visitor = _Visitor(rule: this, source: source);
    source.unit.visitChildren(_visitor);

    return _visitor.issues;
  }
}
